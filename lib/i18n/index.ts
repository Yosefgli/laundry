export function normalizeLocale(value?: string | null): Locale {
  return LOCALES.includes(value as Locale) ? (value as Locale) : DEFAULT_LOCALE;
}

export function localeToIntl(locale: Locale): string {
  if (locale === "he") return "he-IL";
  if (locale === "my") return "my-MM";
  return "en-US";
}

export function mergeTranslations(locale: Locale, map?: TranslationMap | null): TranslationMap {
  return {
    ...FALLBACK_TRANSLATIONS.en,
    ...FALLBACK_TRANSLATIONS[locale],
    ...(map ?? {}),
  };
}

/** Client-side t() — receives pre-loaded map from server. */
export function makeTranslator(map: TranslationMap) {
  return function t(key: string, fallback?: string): string {
    return map[key] ?? fallback ?? key;
  };
}

/** Format currency using locale-aware formatter. */
export function formatCurrency(amount: number, locale: Locale): string {
  return new Intl.NumberFormat(localeToIntl(locale), {
    style: "currency",
    currency: "THB",
    minimumFractionDigits: 2,
  }).format(amount);
}

/** Format weight with locale-aware decimal separator. */
export function formatWeight(kg: number, locale: Locale, unit = "kg"): string {
  return new Intl.NumberFormat(localeToIntl(locale), {
    minimumFractionDigits: 1,
    maximumFractionDigits: 3,
  }).format(kg) + ` ${unit}`;
}
